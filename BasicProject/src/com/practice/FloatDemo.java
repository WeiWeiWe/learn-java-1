package com.practice;

public class FloatDemo {
	public static void main(String[] args) {
		float f = 1234.328f;
		System.out.println("f=" + f);
		double d = 5623.464;
		System.out.println("d=" + d);
		double d1 = 123;
		System.out.println("d1=" + d1); // 123.0
		double d2 = d;
		System.out.println("d2=" + d2);
	}
}
